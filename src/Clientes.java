    public void verclientes(String Nombre)
    {
        Object []O=new Object[8];   
        dtm.setNumRows(0);
        
        try{
           
        enunciado = conexion.prepareStatement("select * from Cliente,Domicilio where Cliente.idCliente=Cliente_idCliente and Cliente.Nombre like'"+Nombre+"%'");
        
         resultado = enunciado.executeQuery();
         while(resultado.next()){
            O[0]=resultado.getInt("idCliente");
            
             O[1]=resultado.getString("NombreCliente");
                     O[2]=resultado.getFloat("MontoGastado");
                     O[3]=resultado.getInt("Visitas");
                     O[4]=resultado.getString("Calle");
                     O[5]=resultado.getString("Callereferencia");
                     O[6]=resultado.getString("Descripcion");
                     O[7]=resultado.getString("Telefono");
                     dtm.addRow(O);
         }
         
      }catch(Exception e){
         e.printStackTrace();
      }
    }
