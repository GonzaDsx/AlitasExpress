        if(c==0)
        {
            
            try
            {
               Integer.parseInt(txtfiltro.getText());
            }catch(NumberFormatException ex)
                    {
                     showMessageDialog(null,"Id no valido.");   
                     return;
                    
                    }
        }
        switch(c)
        {
            case 0:verclientes(Integer.parseInt(txtfiltro.getText())); break;  
             case 1:verclientes(txtfiltro.getText()); break;  
        }
