    public void cargarsalsas()
    {
        Object []O=new Object[2];   
        dtm.setNumRows(0);
        
        try{
           
        enunciado = conexion.prepareStatement("select * from Salsas ");
        
         resultado = enunciado.executeQuery();
         while(resultado.next()){
            O[0]=resultado.getInt("idSalsa");
           O[1]=resultado.getString("Nombre");
              
                     
                     dtm.addRow(O);
         }
         
      }catch(Exception e){
         e.printStackTrace();
      } 
    }
    
