    public void agregar()
    {
         int resul;
     try{ 
         enunciado = conexion.prepareStatement("insert into Salsas(idSalsa,Nombre) values (?,?)");
         enunciado.setInt(1,Integer.parseInt(txtid.getText()));
         enunciado.setString(2,txtnombre.getText());
       
         
         resul = enunciado.executeUpdate();
         if(resul > 0 ){
           
         }
         else{
            JOptionPane.showMessageDialog(null,"Ocurrio un problema al guardar","Error",JOptionPane.ERROR_MESSAGE);
            return;
         }
            }catch(Exception e){
                 e.printStackTrace();
          }   
    }
    public void actualizar()
    {
        int resul;
        try{
         enunciado = conexion.prepareStatement("update Salsas set Nombre=? where idSalsa=? ");
         enunciado.setString(1,txtnombre.getText());
         
         enunciado.setInt(2, Integer.parseInt(txtid.getText()));
         resul = enunciado.executeUpdate();
         
        }catch(Exception e){
         e.printStackTrace();
        }//
    }
